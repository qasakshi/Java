package Switch_Case_Statements;

public class Char_Is_Vowel_Consonant {

	public static void main(String[] args) {
		char c='o';
		
		switch(c){
		case 'a':
			System.out.println("It is vowel");
			break;
			
		case 'e':
			System.out.println("It is vowel");
			break;
			
		case 'i':
			System.out.println("It is vowel");
			break;
			
		case 'o':
			System.out.println("It is vowel");
			break;
			
		case 'u':
			System.out.println("It is vowel");
			break;
			
			default:
				System.out.println("It is consonant");
		}

	}

}
